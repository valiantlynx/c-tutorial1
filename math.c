#include <stdio.h>
#include <math.h>

int main()
{
    /*this function pow() is used to calculate the power of a number */
    printf("\n %f \n", pow(2, 3));

    /*this function sqrt() is used to calculate the square root of a number */
    printf("\n %f \n", sqrt(25));

    /*this function ceil() is used to calculate the ceil of a number */
    printf("\n %f \n", ceil(25.34354534));

    /*this function floor() is used to calculate the floor of a number */
    printf("\n %f \n", floor(25.434));
    
    return 0;
}
